function t(key) {
  var lang = TRANSLATIONS[currentLang];
  if (lang && lang[key] !== undefined) return lang[key];
  // Fallback vers FR, puis la clé brute
  return (TRANSLATIONS['fr'] && TRANSLATIONS['fr'][key] !== undefined)
    ? TRANSLATIONS['fr'][key]
    : key;
}

function setLang(lang) {
  currentLang = lang;
  //console.log('setLang', lang, 'gameCategories.length=', gameCategories.length);
  document.querySelectorAll('.lang-btn').forEach(function(b) {
    b.classList.toggle('active', b.textContent.trim() === lang.toUpperCase());
  });
  document.documentElement.lang = (lang === 'ua') ? 'uk' : lang;
  applyTranslations();
  if (gameCategories.length > 0 && currentStep < gameCountries.length) rerenderCategoryButtonNames();
  rerenderCurrentTurn();
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.innerHTML = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    el.title = t(el.dataset.i18nTitle);
  });
}

